package com.tang.demo1;

public class Cars {
    private String brand;
    private int price;
    private String color;
    public Cars(){}
    public Cars(String brand, int price, String color) {
        this.brand = brand;
        this.price = price;
        this.color = color;
    }
      public String getBrand() {
        return brand;
    }
      public int getPrice() {
        return price;
    }

      public String getColor() {
        return color;
    }







}
